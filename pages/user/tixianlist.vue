<script>
// 引入API函数
import { getWithdrawalRecords } from '@/api/withdrawalAccount.js'

export default {
  name: 'TixianList',
  
  data() {
    return {
      // 加载状态
      loading: false,
      
      // 统计数据 - 从API获取
      totalWithdrawal: '0.00',
      pendingCount: 0,
      monthlyWithdrawal: '0.00',
      
      // 筛选相关
      showFilter: false,
      activeTab: 'all',
      selectedStatus: 'all',
      
      // 筛选标签
      filterTabs: [
        { value: 'all', label: '全部', icon: '📄' },
        { value: 'pending', label: '待审核', icon: '⏳' },
        { value: 'processing', label: '处理中', icon: '🔄' },
        { value: 'completed', label: '已完成', icon: '✅' },
        { value: 'rejected', label: '已拒绝', icon: '❌' }
      ],
      
      // 状态选项
      statusOptions: [
        { value: 'all', label: '全部状态', icon: '📄' },
        { value: 'pending', label: '待审核', icon: '⏳' },
        { value: 'processing', label: '处理中', icon: '🔄' },
        { value: 'completed', label: '已完成', icon: '✅' },
        { value: 'rejected', label: '已拒绝', icon: '❌' }
      ],
      
      // 提现记录数据 - 从API获取
      withdrawalRecords: []
    }
  },
  
  /**
   * 页面加载时获取数据
   */
  onLoad() {
    this.loadWithdrawalRecords()
  },
  
  /**
   * 下拉刷新
   */
  onPullDownRefresh() {
    this.loadWithdrawalRecords().finally(() => {
      uni.stopPullDownRefresh()
    })
  },
  
  methods: {
    /**
     * 加载提现记录数据
     */
    async loadWithdrawalRecords() {
      try {
        this.loading = true
        
        const response = await getWithdrawalRecords({
          status: this.activeTab
        })
        
        // 修正数据结构访问 - 多包了一层
        if (response.data.code === 200) {
          const data = response.data.data
          
          // 更新记录列表
          this.withdrawalRecords = data.records || []
          
          // 更新统计信息
          if (data.statistics) {
            this.totalWithdrawal = data.statistics.total_withdrawal
            this.pendingCount = data.statistics.pending_count
            this.monthlyWithdrawal = data.statistics.monthly_withdrawal
          }
          
          console.log('提现记录加载成功:', data)
        } else {
          uni.showToast({
            title: response.data.message || '获取数据失败',
            icon: 'none'
          })
        }
      } catch (error) {
        console.error('加载提现记录失败:', error)
        uni.showToast({
          title: '网络请求失败',
          icon: 'none'
        })
      } finally {
        this.loading = false
      }
    },
    
    /**
     * 返回上一页
     */
    goBack() {
      uni.navigateBack()
    },
    
    /**
     * 显示筛选弹窗
     */
    showFilterModal() {
      this.showFilter = true
    },
    
    /**
     * 隐藏筛选弹窗
     */
    hideFilterModal() {
      this.showFilter = false
    },
    
    /**
     * 切换标签
     */
    switchTab(tab) {
      if (this.activeTab !== tab) {
        this.activeTab = tab
        this.loadWithdrawalRecords() // 重新加载数据
      }
    },
    
    /**
     * 选择状态
     */
    selectStatus(status) {
      this.selectedStatus = status
    },
    
    /**
     * 重置筛选
     */
    resetFilter() {
      this.selectedStatus = 'all'
    },
    
    /**
     * 应用筛选
     */
    applyFilter() {
      this.activeTab = this.selectedStatus
      this.hideFilterModal()
      this.loadWithdrawalRecords() // 重新加载数据
    },
    
    /**
     * 获取状态样式类
     */
    getStatusClass(status) {
      return `status-${status}`
    },
    
    /**
     * 查看详情
     */
    viewDetail(record) {
      // 可以传递记录数据或ID到详情页面
      const recordData = encodeURIComponent(JSON.stringify({
        id: record.id,
        amount: record.amount,
        status: record.status,
        method: record.method
      }))
      
      uni.navigateTo({
        url: `/pages/withdrawal/detail?data=${recordData}`
      })
    },
    
    /**
     * 跳转到提现页面
     */
    goToWithdrawal() {
      uni.navigateTo({
        url: '/pages/withdrawal/apply'
      })
    }
  }
}
</script>

